package utils;

/**
 * Tuple de deux entiers
 * @author Simon
 */
public class Tuple {
    private int x;
    private int y;

    /**
     * Constructeur par défaut
     * @param x Premier entier
     * @param y Deuxième entier 
     */
    public Tuple(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
